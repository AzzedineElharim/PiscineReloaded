int		ft_sqrt(int nb)
{
	int		tmp;
	int		i;

	i = 1;
	tmp = 1;
	if (nb <= 0)
		return (0);
	while (i * i < nb)
		i++;
	if ((nb % i) == 0)
		return (i);
	else
		return (0);
}
